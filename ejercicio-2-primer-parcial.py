# 2. Escribe un programa que calcule el área y el perímetro de un cuadrado y muestre los resultados (indicando cuál es cuál) por pantalla

lado = float(input("Introduce la longitud del lado del cuadrado: "))

area = lado * lado
perimetro = 4 * lado

print("El área del cuadrado es: ", area)
print("El perímetro del cuadrado es: ", perimetro)